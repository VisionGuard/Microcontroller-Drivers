void PWM1_Generate(uint8 Frequancy , uint8 Duty_Cycle)
{
	ICR1_16BIT_ACCESS = (16000000U / (Frequancy * PWM1_PRESC));
	OCR1A_16BIT_ACCESS = ((Duty_Cycle * (ICR1_16BIT_ACCESS)) /100);
	TCCR1B |= 0x05;
}
